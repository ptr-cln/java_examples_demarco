package org.protor.sandbox.celiento.basic;

public class B {
	
	private boolean b;
	private String[] sa;
	
//------------------------------------------------------
	public B(boolean b, String[] sa) {
		this.b = b;
		this.sa = sa;
	}
	
	public B() {
		
	}
//------------------------------------------------------


	public boolean isB() {
		return b;
	}

	public void setB(boolean b) {
		this.b = b;
	}

	public String[] getSa() {
		return sa;
	}

	public void setSa(String[] sa) {
		this.sa = sa;
	}
	
//------------------------------------------------------

}
